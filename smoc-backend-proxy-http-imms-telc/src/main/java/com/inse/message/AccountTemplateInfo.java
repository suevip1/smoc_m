package com.inse.message;

public class AccountTemplateInfo {
	//模板id
	private String templateId;
	//业务账号
	private String businessAccount;
	//模板标题
	private String templateTitle;
	//模板标识
	private String templateFlag;
	//模板资源
	private String mmAttchnent;
	//模板状态
	private String templateStatus;
	public String getTemplateId() {
		return templateId;
	}
	public void setTemplateId(String templateId) {
		this.templateId = templateId;
	}
	public String getBusinessAccount() {
		return businessAccount;
	}
	public void setBusinessAccount(String businessAccount) {
		this.businessAccount = businessAccount;
	}
	
	public String getTemplateTitle() {
		return templateTitle;
	}
	public void setTemplateTitle(String templateTitle) {
		this.templateTitle = templateTitle;
	}
	public String getTemplateFlag() {
		return templateFlag;
	}
	public void setTemplateFlag(String templateFlag) {
		this.templateFlag = templateFlag;
	}
	public String getMmAttchnent() {
		return mmAttchnent;
	}
	public void setMmAttchnent(String mmAttchnent) {
		this.mmAttchnent = mmAttchnent;
	}
	public String getTemplateStatus() {
		return templateStatus;
	}
	public void setTemplateStatus(String templateStatus) {
		this.templateStatus = templateStatus;
	}
	
	
	

}
